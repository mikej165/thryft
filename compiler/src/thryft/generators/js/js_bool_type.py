from thryft.generator.bool_type import BoolType
from thryft.generators.js._js_base_type import _JsBaseType


class JsBoolType(BoolType, _JsBaseType):
    def js_default_value(self):
        return 'false'

    def js_literal(self, value):
        return value and 'true' or 'false'

    def js_name(self):
        return 'boolean'

    def js_qname(self):
        return 'boolean'

    def js_schema(self):
        return {'type': 'Checkboxes', 'options': ['']}

    def js_validation(self, value, value_name, **kwds):
        return {'oneOf': (True, False),
                'type': """\
if (typeof %(value)s !== "boolean") {
    return "expected %(value_name)s to be a boolean";
}""" % locals()}
